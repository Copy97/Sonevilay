    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
        // TODO add your handling code here:
        if (valueA == "A") {
            double a = Double.parseDouble(txtresult.getText());
            show = show + a;
            valueA = "";
            valueB = "A";
        } else if (valueA == "B") {
            double a = Double.parseDouble(txtresult.getText());
            show = show - a;
            valueA = "";
            valueB = "B";
        } else if (valueA == "C") {
            double a = Double.parseDouble(txtresult.getText());
            show = show * a;
            valueA = "";
            valueB = "C";
        } else if (valueA == "D") {
            double a = Double.parseDouble(txtresult.getText());
            show = show / a;
            valueA = "";
            valueB = "D";
        }
         String a=String.valueOf(show);
         int length=a.length();
         String b=a.substring(length-2);
         if(b.equals(".0")){
         String c=a.substring(0,length-2);
         txtresult.setText(c);
         }
         else{
         txtresult.setText(a);
         }
    }//GEN-LAST:event_equalActionPerformed

