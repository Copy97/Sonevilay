    private void multiplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_multiplyActionPerformed
        // TODO add your handling code here:
        if(valueA=="A" && valueC=="1"){
         double a = Double.parseDouble(txtresult.getText());
         show = show+a;
         String b=String.valueOf(show);
         int length=b.length();
         String sub=b.substring(length-2);
         if(sub.equals(".0")){
          String sub1=b.substring(0,length-2);
          txtresult.setText(sub1);
         }else{
         txtresult.setText(b);
         }
        }
         else if (valueA=="B" && valueC=="1"){
         double a = Double.parseDouble(txtresult.getText());
         show = show-a;
         String b=String.valueOf(show);
         int length=b.length();
         String sub=b.substring(length-2);
         if(sub.equals(".0")){
          String sub1=b.substring(0,length-2);
          txtresult.setText(sub1);
        }else{
         txtresult.setText(b);
         }
         }
         else if (valueA=="C" && valueC=="1"){
         double a = Double.parseDouble(txtresult.getText());
         show = show*a;
         String b=String.valueOf(show);
         int length=b.length();
         String sub=b.substring(length-2);
         if(sub.equals(".0")){
          String sub1=b.substring(0,length-2);
          txtresult.setText(sub1);
        }else{
         txtresult.setText(b);
         }
         }
          else if (valueA=="D" && valueC=="1"){
         double a = Double.parseDouble(txtresult.getText());
         show = show/a;
         String b=String.valueOf(show);
         int length=b.length();
         String sub=b.substring(length-2);
         if(sub.equals(".0")){
          String sub1=b.substring(0,length-2);
          txtresult.setText(sub1);
        }else{
         txtresult.setText(b);
         }
         }
        double a = Double.parseDouble(txtresult.getText());
        show = a;
        valueA = "C";
        valueB = "C";
        valueC="0";
    }//GEN-LAST:event_multiplyActionPerformed

    private void num10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_num10ActionPerformed
        // TODO add your handling code here:
        String a=String.valueOf(txtresult.getText());
        int length=a.length();
        int b=1;
        for(int i=length; i>0;i--){
         String sub=a.substring(i-1,i);
         if(".".equals(sub)){
             b=0;
             i=0;
         }
        }
        if (txtresult.getText() == "") {
            txtresult.setText(String.valueOf("0."));
        } else if (b!=0){
            String n = txtresult.getText();
            txtresult.setText(String.valueOf(n + "."));
        }
    }//GEN-LAST:event_num10ActionPerformed

